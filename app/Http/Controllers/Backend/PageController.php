    public function checkActive($page_id){
        $user=Page::find($page_id);
        //toggle the is active
        if($user->is_active == 1){
            $user->is_active = 0;
        }else{
            $user->is_active = 1;
        }

        $user->update();
        return response()->json(['type' => 'success', 'message' => 'Status Updated']);
    }
